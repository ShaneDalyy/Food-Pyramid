function incrementButton1()
{
    var element = document.getElementById('IncrementText1');
    var value = element.innerHTML;
    value++;

    console.log(value);
    document.getElementById('IncrementText1').innerHTML=value;

}
function decrementButton1()
{
    var element2 = document.getElementById('IncrementText1');
    var value2 = element2.innerHTML;
    if(value2 >0)
    {
        value2--;
    }

    console.log(value2);
    document.getElementById('IncrementText1').innerHTML=value2;
}
